--  DDL for Procedure GET_BUDGETS_WITH_SPENT_PROC
--------------------------------------------------------
set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "ADMIN"."GET_BUDGETS_WITH_SPENT_PROC" (
    p_user_id IN NUMBER,
    p_result  OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN p_result FOR
        SELECT b.ID AS id,
               b.CATEGORY AS category,
               b.MONTHLY_LIMIT AS monthly_limit,
               NVL(SUM(t.AMOUNT), 0) AS spent,
               b.CREATED_AT AS created_at
        FROM BUDGETS b
        LEFT JOIN TRANSACTIONS t
            ON t.USER_ID = b.USER_ID
           AND t.CATEGORY = b.CATEGORY
           AND t.TYPE = 'EXPENSE'
           AND EXTRACT(MONTH FROM t.TRANSACTION_DATE) = EXTRACT(MONTH FROM CURRENT_DATE)
           AND EXTRACT(YEAR FROM t.TRANSACTION_DATE) = EXTRACT(YEAR FROM CURRENT_DATE)
        WHERE b.USER_ID = p_user_id
        GROUP BY b.ID, b.CATEGORY, b.MONTHLY_LIMIT, b.CREATED_AT
        ORDER BY b.CREATED_AT;
END;

