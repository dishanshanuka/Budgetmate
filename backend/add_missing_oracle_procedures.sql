CREATE OR REPLACE PROCEDURE get_user_accounts_proc (
    p_user_id IN NUMBER,
    p_result  OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN p_result FOR
        SELECT account_id AS id,
               account_name,
               account_type,
               balance,
               COALESCE(card_number, card_number_masked) AS card_number,
               expiry_date,
               color_theme,
               CAST(NULL AS TIMESTAMP) AS created_at
        FROM accounts
        WHERE user_id = p_user_id
        ORDER BY account_id;
END;
/

CREATE OR REPLACE PROCEDURE add_account_proc (
    p_user_id       IN  NUMBER,
    p_account_name  IN  VARCHAR2,
    p_account_type  IN  VARCHAR2,
    p_balance       IN  NUMBER,
    p_card_number   IN  VARCHAR2 DEFAULT NULL,
    p_expiry_date   IN  VARCHAR2 DEFAULT NULL,
    p_color_theme   IN  VARCHAR2 DEFAULT NULL,
    p_account_id    OUT NUMBER
) AS
BEGIN
    INSERT INTO accounts
        (user_id, account_name, account_type, balance, card_number,
         card_number_masked, expiry_date, color_theme)
    VALUES
        (p_user_id, p_account_name, p_account_type, p_balance, p_card_number,
         p_card_number, p_expiry_date, p_color_theme)
    RETURNING account_id INTO p_account_id;

    COMMIT;
END;
/

CREATE OR REPLACE PROCEDURE delete_account_proc (
    p_account_id    IN  NUMBER,
    p_user_id       IN  NUMBER,
    p_rows_affected OUT NUMBER
) AS
BEGIN
    DELETE FROM transactions
    WHERE account_id = p_account_id
      AND user_id = p_user_id;

    DELETE FROM accounts
    WHERE account_id = p_account_id
      AND user_id = p_user_id;

    p_rows_affected := SQL%ROWCOUNT;
    COMMIT;
END;
/

