CREATE OR REPLACE PROCEDURE get_profile_proc (
    p_user_id       IN  NUMBER,
    p_full_name     OUT VARCHAR2,
    p_email         OUT VARCHAR2,
    p_profile_photo OUT CLOB,
    p_status        OUT VARCHAR2
) AS
BEGIN
    SELECT full_name, email, profile_photo
    INTO p_full_name, p_email, p_profile_photo
    FROM users
    WHERE id = p_user_id;

    p_status := 'SUCCESS';
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        p_status := 'NOT_FOUND';
    WHEN OTHERS THEN
        p_status := 'ERROR';
END;
/

CREATE OR REPLACE PROCEDURE update_profile_proc (
    p_user_id        IN  NUMBER,
    p_full_name      IN  VARCHAR2,
    p_profile_photo  IN  CLOB,
    p_rows_affected  OUT NUMBER
) AS
BEGIN
    UPDATE users
    SET full_name = p_full_name,
        profile_photo = p_profile_photo
    WHERE id = p_user_id;

    p_rows_affected := SQL%ROWCOUNT;
    COMMIT;
END;
/

CREATE OR REPLACE PROCEDURE get_user_password_hash_proc (
    p_user_id       IN  NUMBER,
    p_password_hash OUT VARCHAR2,
    p_status        OUT VARCHAR2
) AS
BEGIN
    SELECT password_hash
    INTO p_password_hash
    FROM users
    WHERE id = p_user_id;

    p_status := 'SUCCESS';
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        p_status := 'NOT_FOUND';
    WHEN OTHERS THEN
        p_status := 'ERROR';
END;
/

CREATE OR REPLACE PROCEDURE change_password_proc (
    p_user_id       IN  NUMBER,
    p_password_hash IN  VARCHAR2,
    p_status        OUT VARCHAR2
) AS
BEGIN
    UPDATE users
    SET password_hash = p_password_hash
    WHERE id = p_user_id;

    IF SQL%ROWCOUNT > 0 THEN
        COMMIT;
        p_status := 'SUCCESS';
    ELSE
        p_status := 'NOT_FOUND';
    END IF;
EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        p_status := 'ERROR';
END;
/
