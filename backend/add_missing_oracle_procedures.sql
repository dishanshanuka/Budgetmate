CREATE OR REPLACE PROCEDURE add_transaction_proc (
    p_user_id          IN  NUMBER,
    p_account_id       IN  NUMBER,
    p_description      IN  VARCHAR2,
    p_category         IN  VARCHAR2,
    p_amount           IN  NUMBER,
    p_transaction_type IN  VARCHAR2,
    p_status           OUT VARCHAR2
) AS
    v_count NUMBER;
BEGIN
    SELECT COUNT(*)
    INTO v_count
    FROM accounts
    WHERE account_id = p_account_id
      AND user_id = p_user_id;

    IF v_count = 0 THEN
        p_status := 'ACCOUNT_NOT_FOUND';
        RETURN;
    END IF;

    INSERT INTO transactions
        (account_id, user_id, description, category, amount, type)
    VALUES
        (p_account_id, p_user_id, p_description, p_category, p_amount,
         p_transaction_type);

    COMMIT;
    p_status := 'SUCCESS';
EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        IF SQLCODE = -20001 THEN
            p_status := 'INSUFFICIENT_BALANCE';
        ELSE
            p_status := 'FAILED';
        END IF;
END;
/

CREATE OR REPLACE PROCEDURE get_user_transactions_proc (
    p_user_id IN NUMBER,
    p_result  OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN p_result FOR
        SELECT transaction_id AS id,
               user_id,
               account_id,
               description AS title,
               category,
               amount,
               type AS transaction_type,
               transaction_date
        FROM transactions
        WHERE user_id = p_user_id
        ORDER BY transaction_date DESC, transaction_id DESC;
END;
/

